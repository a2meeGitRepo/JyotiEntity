package com.a2mee.model.dto;

public class PoDto {

	private long poId;
	private double poQty;
	private long disPoNo;
	public long getPoId() {
		return poId;
	}
	public void setPoId(long poId) {
		this.poId = poId;
	}
	public double getPoQty() {
		return poQty;
	}
	public void setPoQty(double poQty) {
		this.poQty = poQty;
	}
	public long getDisPoNo() {
		return disPoNo;
	}
	public void setDisPoNo(long disPoNo) {
		this.disPoNo = disPoNo;
	}
	
	
}
